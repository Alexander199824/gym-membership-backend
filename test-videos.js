  async testFileUpload(filePath) {
    console.log(`\n5. üì§ Probando subida de archivo: ${path.basename(filePath)}`);
    
    // Verificar que el archivo existe
    if (!fs.existsSync(filePath)) {
      throw new Error(`El archivo no existe: ${filePath}`);
    }

    const stats = fs.statSync(filePath);
    const sizeKB = (stats.size / 1024).toFixed(2);
    const sizeMB = (stats.size / (1024 * 1024)).toFixed(2);
    
    console.log(`   üìè Tama√±o: ${sizeKB} KB (${sizeMB} MB)`);

    // Determinar tipo de archivo y endpoint
    const ext = path.extname(filePath).toLowerCase();
    const { fileType, endpoint, maxSize } = this.determineFileType(ext);
    
    console.log(`   üîç Tipo detectado: ${fileType}`);
    console.log(`   üéØ Endpoint: ${endpoint}`);

    if (stats.size > maxSize) {
      throw new Error(`Archivo demasiado grande (m√°ximo ${maxSize / (1024 * 1024)}MB para ${fileType})`);
    }

    try {
      const form = new FormData();
      
      // Determinar el nombre del campo seg√∫n el tipo
      let fieldName = 'file';
      if (fileType === 'video') fieldName = 'video';
      else if (fileType === 'logo') fieldName = 'logo';
      else if (fileType === 'imagen') fieldName = 'image';
      
      form.append(fieldName, fs.createReadStream(filePath), {
        filename: path.basename(filePath),
        contentType: this.getContentType(filePath)
      });

      console.log(`   üì° Subiendo ${fileType} a Cloudinary...`);

      const uploadResponse = await axios.post(
        `${this.baseURL}${endpoint}`,
        form,
        {
          headers: {
            ...form.getHeaders(),
            'Authorization': `Bearer ${this.token}`
          },
          timeout: 60000 // 60 segundos para videos
        }
      );

      if (uploadResponse.data.success) {
        console.log(`   üéâ ¬°${fileType} subido exitosamente!`);
        
        const data = uploadResponse.data.data;
        
        if (fileType === 'video') {
          console.log(`   üîó Video URL: ${data.videoUrl}`);
          console.log(`   üñºÔ∏è Poster URL: ${data.posterUrl || 'Generado autom√°ticamente'}`);
        } else {
          console.log(`   üîó URL: ${data.logoUrl || data.imageUrl || data.url}`);
        }
        
        console.log(`   üÜî Public ID: ${data.publicId}`);
        
        // Verificar que es de Cloudinary
        const url = data.videoUrl || data.logoUrl || data.imageUrl || data.url;
        if (url && url.includes('cloudinary.com')) {
          console.log('   ‚òÅÔ∏è ‚úÖ Confirmado: Almacenado en Cloudinary');
        }
        
        return data;
      } else {
        throw new Error('Error en la respuesta de subida');
      }

    } catch (error) {
      if (error.response) {
        if (error.response.status === 503) {
          throw new Error('Servicio de archivos no configurado');
        } else if (error.response.status === 400) {
          throw new Error(`Error de validaci√≥n: ${error.response.data.message}`);
        } else if (error.response.status === 404) {
          throw new Error(`Endpoint no encontrado: ${endpoint}. ¬øHas agregado las rutas de gym-media?`);
        } else {
          throw new Error(`Error HTTP ${error.response.status}: ${error.response.data?.message || 'Error desconocido'}`);
        }
      } else if (error.code === 'ECONNABORTED') {
        throw new Error('Timeout - El archivo tard√≥ demasiado en subir');
      } else {
        throw new Error(`Error subiendo archivo: ${error.message}`);
      }
    }
  }

  determineFileType(ext) {
    const videoExts = ['.mp4', '.webm', '.mov', '.avi'];
    const imageExts = ['.jpg', '.jpeg', '.png', '.gif', '.webp'];
    
    if (videoExts.includes(ext)) {
      return {
        fileType: 'video',
        endpoint: '/api/gym-media/upload-hero-video',
        maxSize: 50 * 1024 * 1024 // 50MB
      };
    } else if (imageExts.includes(ext)) {
      // Por defecto, tratamos im√°genes como logo, pero podr√≠as hacer esto m√°s inteligente
      return {
        fileType: 'logo', // o 'imagen' dependiendo de lo que quieras probar
        endpoint: '/api/gym-media/upload-logo', // o '/api/gym-media/upload-hero-image'
        maxSize: 5 * 1024 * 1024 // 5MB
      };
    } else {
      throw new Error(`Formato no soportado: ${ext}`);
    }
  }

