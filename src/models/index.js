// ‚úÖ FUNCI√ìN DE SINCRONIZACI√ìN MEJORADA Y COMPLETA
const syncDatabase = async (options = {}) => {
  console.log('üîÑ Iniciando sincronizaci√≥n completa de base de datos...');
  
  try {
    await sequelize.authenticate();
    console.log('‚úÖ Conexi√≥n verificada');

    // Obtener todos los modelos cargados
    const allLoadedModels = Object.keys(db).filter(key => 
      !['sequelize', 'Sequelize', 'diagnose', 'verifyFlexibleScheduleModels', 'syncDatabase', 'resetDatabase', 'checkDatabaseStatus', 'initializeForDevelopment', 'fullDiagnosis', 'repairPaymentModel', 'repairProblematicTables', 'syncDatabaseWithRepair', 'initializeData', 'validateModels', 'getModelsInfo'].includes(key)
    );

    console.log(`üìä TODOS los modelos cargados: ${allLoadedModels.length}`);
    console.log(`üìã Lista completa: ${allLoadedModels.join(', ')}`);

    // Detectar si estamos en desarrollo
    const isDevelopment = process.env.NODE_ENV === 'development' || options.forceDevelopmentMode || options.force;
    const syncOptions = isDevelopment ? { alter: !options.force, force: options.force, ...options } : options;
    
    console.log(`üîß Modo de sincronizaci√≥n: ${isDevelopment ? `DESARROLLO (${options.force ? 'force: true' : 'alter: true'})` : 'PRODUCCI√ìN'}`);

    // Orden espec√≠fico para modelos cr√≠ticos
    const prioritySyncOrder = [
      'User', 'MembershipPlans', 'StoreBrand', 'StoreCategory', 'DailyIncome', 'GymConfiguration', 'GymHours',
      'GymTimeSlots', 'Membership', 'StoreProduct', 'Payment', 'StoreProductImage', 'StoreCart', 'StoreOrder',
      'StoreOrderItem', 'FinancialMovements', 'LocalSale', 'LocalSaleItem', 'TransferConfirmation'
    ];

    // Agregar modelos que no est√°n en la lista de prioridad
    const remainingModels = allLoadedModels.filter(model => !prioritySyncOrder.includes(model));
    const fullSyncOrder = [...prioritySyncOrder, ...remainingModels];

    console.log(`üîÑ Sincronizando ${fullSyncOrder.length} modelos total...`);

    let syncSuccess = 0;
    let syncErrors = 0;
    let alteredTables = [];
    let repairedTables = [];

    for (const modelName of fullSyncOrder) {
      if (db[modelName]) {
        try {
          console.log(`üîÑ Sincronizando ${modelName}...`);
          
          await db[modelName].sync(syncOptions);
          console.log(`‚úÖ ${modelName} sincronizado`);
          
          if (syncOptions.alter || syncOptions.force) {
            alteredTables.push(modelName);
          }
          
          syncSuccess++;
          
        } catch (error) {
          console.error(`‚ùå Error en ${modelName}:`, error.message);
          
          // Detectar problemas espec√≠ficos y intentar reparaciones
          const isEnumUniqueError = error.message.includes('syntax error at or near "UNIQUE"') ||
                                   error.message.includes('ENUM') && error.message.includes('unique');
          
          if (isEnumUniqueError && (modelName === 'GymFormsConfig' || modelName === 'GymSocialMedia')) {
            console.log(`üîß ${modelName}: Detectado problema ENUM+UNIQUE, reparando...`);
            
            try {
              const repairResult = await repairProblematicTables();
              if (repairResult.success) {
                console.log(`   ‚úÖ ${modelName} reparado por funci√≥n espec√≠fica`);
                repairedTables.push(modelName);
                syncSuccess++;
                continue;
              }
            } catch (repairError) {
              console.error(`   ‚ùå Error en reparaci√≥n espec√≠fica:`, repairError.message);
            }
          }
          
          // Intentos de recuperaci√≥n
          if (error.message.includes('does not exist') || 
              error.message.includes('column') || 
              error.message.includes('relation')) {
            
            console.log(`üîÑ ${modelName}: Detectado error de esquema, forzando ALTER...`);
            
            try {
              await db[modelName].sync({ alter: true, force: false });
              console.log(`‚ö†Ô∏è ${modelName} sincronizado con ALTER forzado`);
              alteredTables.push(modelName);
              syncSuccess++;
            } catch (alterError) {
              console.error(`‚ùå ${modelName}: Error persistente despu√©s de ALTER:`, alterError.message);
              
              if (isDevelopment && (alterError.message.includes('constraint') || alterError.message.includes('type'))) {
                console.log(`üîÑ ${modelName}: √öltimo recurso - recreando tabla...`);
                try {
                  await db[modelName].drop({ cascade: true });
                  await db[modelName].sync({ force: false });
                  console.log(`‚ö†Ô∏è ${modelName} recreado completamente`);
                  repairedTables.push(modelName);
                  syncSuccess++;
                } catch (recreateError) {
                  console.error(`‚ùå ${modelName}: Error incluso recreando:`, recreateError.message);
                  syncErrors++;
                }
              } else {
                syncErrors++;
              }
            }
          } else {
            syncErrors++;
          }
        }
      } else {
        console.log(`‚ö†Ô∏è Modelo ${modelName} no encontrado en db - omitiendo`);
      }
    }

    console.log(`üìä Resumen de sincronizaci√≥n:`);
    console.log(`   ‚úÖ Exitosos: ${syncSuccess}`);
    console.log(`   ‚ùå Con errores: ${syncErrors}`);
    console.log(`   üìã Total procesados: ${syncSuccess + syncErrors}`);
    
    if (alteredTables.length > 0) {
      console.log(`üîß Tablas alteradas/recreadas: ${alteredTables.join(', ')}`);
    }
    
    if (repairedTables.length > 0) {
      console.log(`üõ†Ô∏è Tablas reparadas: ${repairedTables.join(', ')}`);
    }

    // Crear datos iniciales si es force o no existen datos
    if (syncOptions.force || options.initializeData) {
      await initializeData();
    }

    console.log('üéâ Sincronizaci√≥n completada exitosamente');
    return { 
      success: true, 
      message: 'Base de datos sincronizada correctamente',
      stats: {
        totalModels: fullSyncOrder.length,
        syncSuccess,
        syncErrors,
        modelsFound: allLoadedModels.length,
        alteredTables: alteredTables.length,
        repairedTables: repairedTables.length,
        syncMode: isDevelopment ? 'development' : 'production'
      }
    };

  } catch (error) {
    console.error('‚ùå Error en sincronizaci√≥n:', error.message);
    return { success: false, error: error.message };
  }
};

