  async getAllPayments() {
    console.log('\n6. üí∞ Obteniendo TODOS los pagos con informaci√≥n completa...');
    
    try {
      // Obtener todos los pagos (incluyendo todos los estados)
      const response = await axios.get(`${this.baseURL}/api/payments`, {
        headers: { 'Authorization': `Bearer ${this.adminToken}` },
        params: {
          limit: 100,
          page: 1,
          includeAll: 'true' // ‚úÖ NUEVO: Para incluir todos los estados
        }
      });

      if (response.data.success) {
        this.allPayments = response.data.data.payments;
        const { pagination } = response.data.data;
        
        console.log(`   ‚úÖ Pagos obtenidos: ${this.allPayments.length} de ${pagination.total} total`);
        console.log(`   üìä P√°ginas disponibles: ${pagination.pages}`);
        
        // Si hay m√°s p√°ginas, obtener todas
        if (pagination.pages > 1) {
          console.log('   üìÑ Obteniendo p√°ginas adicionales...');
          
          for (let page = 2; page <= pagination.pages; page++) {
            const pageResponse = await axios.get(`${this.baseURL}/api/payments`, {
              headers: { 'Authorization': `Bearer ${this.adminToken}` },
              params: { 
                limit: 100, 
                page,
                includeAll: 'true'
              }
            });
            
            if (pageResponse.data.success) {
              this.allPayments.push(...pageResponse.data.data.payments);
              console.log(`      P√°gina ${page}: +${pageResponse.data.data.payments.length} pagos`);
            }
          }
        }
        
        console.log(`   üéØ TOTAL DE PAGOS OBTENIDOS: ${this.allPayments.length}`);
        
        if (this.allPayments.length === 0) {
          console.log('   üìù No hay pagos registrados en el sistema');
        }
        
      } else {
        throw new Error('Respuesta sin √©xito al obtener pagos');
      }
    } catch (error) {
      throw new Error(`Error obteniendo pagos: ${error.response?.data?.message || error.message}`);
    }
  }

